//Assignment Operator

// =

// +=
var num = 50;
document.write('<h3> ', "50 += 30 :  ", num += 30, ' </h3>');

// -=
var num = 50;
document.write('<h3> ', "50 -= 30 :  ", num -= 30, ' </h3>');


// /=
var num = 50;
document.write('<h3> ', "50 /= 30 :  ", num /= 30, ' </h3>');


// *=
var num = 50;
document.write('<h3> ', "50 *= 30 :  ", num *= 30, ' </h3>');
